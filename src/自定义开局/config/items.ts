// ============ 自创资材点数计算 ============
const QUALITY_RANK: Record<ItemQuality, number> = {
  凡: 1,
  黄: 2,
  玄: 4,
  地: 8,
  天: 14,
};
const REALM_RANK: Record<ItemRealm, number> = {
  炼气: 1,
  筑基: 2,
  金丹: 4,
  元婴: 7,
  化神: 11,
};

/**
 * 自创物品点数 = ceil((品质秩 + 境界秩) × 1.3) + 1
 * 比同档预设大致贵 30%~50%，以体现"自定"溢价。
 */
export function computeCustomItemCost(input: {
  品质: ItemQuality;
  境界: ItemRealm;
}): number {
  const q = QUALITY_RANK[input.品质] ?? 1;
  const r = REALM_RANK[input.境界] ?? 1;
  return Math.ceil((q + r) * 1.3) + 1;
}

export function customItemToOption(c: CustomItem): ItemOption {
  return {
    id: c.id,
    name: c.name,
    subtitle: `${c.品质}品 · ${c.境界} · ${c.类型}`,
    desc: c.desc || '玩家自创资材。',
    cost: computeCustomItemCost({ 品质: c.品质, 境界: c.境界 }),
    category: c.category,
    类型: c.类型,
    品质: c.品质,
    境界: c.境界,
    五行: c.五行,
    glyph: '自',
    tags: ['自创'],
    data: {
      品质: c.品质,
      境界: c.境界,
      类型: c.类型,
      五行: c.五行,
      自创: true,
    },
  };
}

